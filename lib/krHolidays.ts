export function getKrHolidays(year: number): Set<string> {
  const key = String(year)
  const list = HOLIDAYS_BY_YEAR[key]
  if (!list) {
    if (typeof console !== 'undefined') {
      console.warn(`[krHolidays] ${year}년 공휴일 데이터가 없습니다. lib/krHolidays.ts에 추가해주세요.`)
    }
    return new Set()
  }
  return new Set(list)
}

// 주말·공휴일 회피 — 자동이체 실제 이체일 계산
export function getNextBusinessDay(baseDate: Date): Date {
  const d = new Date(baseDate)
  while (true) {
    const dow = d.getDay()
    if (dow === 0) { d.setDate(d.getDate() + 1); continue } // 일요일
    if (dow === 6) { d.setDate(d.getDate() + 2); continue } // 토요일
    const iso = `${d.getFullYear()}-${String(d.getMonth()+1).padStart(2,'0')}-${String(d.getDate()).padStart(2,'0')}`
    const holidays = getKrHolidays(d.getFullYear())
    if (holidays.has(iso)) { d.setDate(d.getDate() + 1); continue }
    break
  }
  return d
}
